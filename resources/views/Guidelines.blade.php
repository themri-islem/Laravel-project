            <h3 class="mb-2">Oral Presentations</h3>

            <p>
                Regarding oral presentations (i.e. papers included in sessions, special sessions and student contest sessions), 
                the slot for each paper is 15 minutes (10-12 minutes for presentation and 3-5 minutes for questions).  
                A computer with PDF reader and Microsoft PowerPoint will be available. 
                All presenters should pass their presentations to the technical staff, present in each room, 
                using their own external storage devices during the break or lunch before the sessions, 
                in which their presentations are included.
            </p>

            <h3 class="mb-2">Poster Presentations</h3>

            <p>
                The required poster size is A0, portrait orientation. Poster authors are expected 
                to present their posters to the scientific community at the dedicated poster sessions. 
                Lunch and coffee breaks could also be opportunities to present the posters. 
                Please take the opportunity to read other posters and exchange contact information. 
                Poster presenters may want to have A4 copies of their posters and their business cards available for distribution.
            </p>

            <h3 class="mb-2">Poster Mounting</h3>

            <p>
                All posters shall be mounted ahead of the poster session. 
                Materials for mounting posters will be provided at the poster session venue. 
                Please check that your poster is located on the correct board. 
                Your poster number is the same to the number of your submission (see <a class="text-decoration-underline" href="program.html" target="_blank">Program</a>). 
                The list of poster numbers will be also available in the conference room. 
                Posters shall be dismounted by the end of the day, those that have not been removed will be disposed-off.
            </p>

            <h3 class="mb-2">Printing your poster in Heraklion</h3>

            <p>
                If you would like to print your poster in Heraklion, you can choose the stationery store "Aplada", 
                which is located at <a class="text-decoration-underline" href="https://goo.gl/maps/XJdkSZGtmTqNQtVn8" target="_blank">62 Evans str.</a> close to the JURSE 2023 venue. 
                You can choose between two paper types: plain (17 euros) or glossy (30 euros). 
                You will need to send a pdf version of your poster by Friday May 12 at <a class="text-decoration-underline" href="mailto:evans@aplada.gr?subject=JURSE 2023">evans@aplada.gr</a> using "JURSE 2023" in 
                the subject of the email and informing the store about the selected printing paper type. 
                As soon as your poster is printed, you will receive email notification by the store in order to 
                receive it from the store at your convenience. The payment of the poster will be completed at the store.
            </p>

