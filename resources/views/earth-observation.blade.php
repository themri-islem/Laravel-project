                        <b>Abstract</b>
                    </p>
                    <p align="justify">
                        This session will gather contributions and experiences of optimising, integrating and 
                        exploiting existing Earth Observation (EO) systems to support processes of urban 
                        adaptation to Climate Change (CC). Indeed, seeing the current lack of a dedicated process 
                        of understanding and quantifying CC effects on urban areas and the lack of engagement 
                        between the urban planners and the EO community identified by UN Habitat, the session 
                        will focus the contribution of EO to the integrated and holistic assessment of risks 
                        related to CC and other extreme events to be taken into account in the sustainable 
                        planning of cities, consistently with the objectives set by the Paris Agreement. 
                        Referring to the framework of the nine Societal Benefits Areas (SBAs) that clearly 
                        posits the role of EO as a crucial tool towards resilient cities and to promote equity, 
                        welfare and shared prosperity for all, new indicators and integradet methodologies for 
                        the monitoring of progress towards the Sustainable Development Goals in an EU context 
                        are needed. In particular the selection of contributions will aim at exploring how 
                        Remote Sensing tools, 3D-4D monitoring and Machine Learning/Deep Learning techniques 
                        can be effectively integrated in the urban decision-making processes to support urban 
                        development and to deliver real benefits.
                    </p>
